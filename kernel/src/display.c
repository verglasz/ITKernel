/* quicksleep:
   A simple function to create a small delay.
   Very inefficient use of computing resources,
   but very handy in some special cases. */
void quicksleep(int cyc) {
	int i;
	for(i = cyc; i > 0; i--);
}


/* spi_send_recv:
	Send data to the SPI2 bus
*/
uint8_t spi_send_recv(uint8_t data) {
	// Wait for buffer to be ready
	while(!(SPI2STAT & 0x08));
	// Put the data on the buffer
	SPI2BUF = data;
	// Wait for buffer response.
	while(!(SPI2STAT & 1));
	// Return buffer response.
	return SPI2BUF;
}
void display_clear(void) {
    int i;
    for (i = 0; i < DISPLAY_BUFFER_SIZE; i++)
        displaybuffer[i] = 0;
}

void display_init(void) {
	/*
	Power on sequence:
		1. Apply power to VDD.
		2. Send Display Off command.
		3. Initialize display to desired operating mode.
		4. Clear screen.
		5. Apply power to VBAT.
		6. Delay 100ms.
		7. Send Display On command.
	*/

	DISPLAY_CHANGE_TO_COMMAND_MODE;

	// Startup sequence
	quicksleep(100);
	DISPLAY_ACTIVATE_VDD;
	quicksleep(1000000);
	// Turn display off
	spi_send_recv(0xAE);
	
	// Bring reset low, wait for driver to reset then turn reset high.
	DISPLAY_ACTIVATE_RESET;
	quicksleep(100);
	DISPLAY_DO_NOT_RESET;
	quicksleep(100);
	// End of startup sequence

	// Charge pump setting
	spi_send_recv(0x8D);
	// Value
	spi_send_recv(0x14);
	// Pre-charge period
	spi_send_recv(0xD9);
	// Num of CLK
	spi_send_recv(0xF1);


	DISPLAY_ACTIVATE_VBAT;
	quicksleep(1000000);

	// Sets the mapping to display data column adress (Put origin in top-left corner)
	spi_send_recv(0xA1);
	spi_send_recv(0xC8);

	// Set COM pins config
	spi_send_recv(0xDA);
	// OLED config
	spi_send_recv(0x20);


	// Turn on screen (normal mode) | (0xAE for sleep mode)
	spi_send_recv(0xAF);

}

void display_string(int line, char *s) {
	int i;
	if(line < 0 || line >= 4)
		return;
	if(!s)
		return;
	
	for(i = 0; i < 16; i++)
		if(*s) {
			textbuffer[line][i] = *s;
			s++;
		} else
			textbuffer[line][i] = ' ';
}

